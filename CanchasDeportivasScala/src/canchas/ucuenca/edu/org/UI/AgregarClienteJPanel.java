    private void aceptarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarBtnActionPerformed
        // TODO add your handling code here:
        String cedula = cedulaTxt.getText();
        String nombre = nombreTxt.getText();
        String apellido = apellidoTxt.getText();
        String edad = edadTxt.getText();

        try {
            String mensaje = null;
            int cedulaInt = Integer.parseInt(cedula);
            int edadInt = Integer.parseInt(edad);

            ClienteSrv service = new ClienteSrv();

            if(modoEdicion.equals(ModoEdicion.AGREGAR)){
                service.crearCliente(cedula, nombre, apellido, edadInt);
                service.guardar();
                mensaje = "Cliente agregado correctamente";
                this.setVisible(false);
            }

            else if(modoEdicion.equals(ModoEdicion.MODIFICAR)){
                service.modificar(cedula, nombre, apellido, edadInt);

                service.guardar();

                mensaje = "Cliente modificado correctamente";
            }

            else if(modoEdicion.equals(ModoEdicion.ELIMINAR)){
                service.eliminarEncargado(cedula);
                service.guardar();
                mensaje = "Cliente Eliminado correctamente";
            }

            JOptionPane.showMessageDialog(null, mensaje);
            this.hide();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error en los campos!");
        } catch (IllegalArgumentException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_aceptarBtnActionPerformed

