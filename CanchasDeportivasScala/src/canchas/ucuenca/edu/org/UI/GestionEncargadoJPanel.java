    private void cargarTabla() {
        
        limpiarTabla();
        try {
            modelo = (DefaultTableModel) tablaEncargadosTBL.getModel();
            EncargadoSrv servi = new EncargadoSrv();
            
            String[] lista = servi.listar().split("_");
            String[] canchas = null;
            
            for (int i = 0; i<lista.length; i++) {
                canchas = lista[i].split(",");
                
                
                modelo.addRow(canchas);
            }
            
            tablaEncargadosTBL.setModel(modelo);
            
        } catch (Exception e) {
        }
        
    }
    
    private void limpiarTabla(){
        modelo = (DefaultTableModel) tablaEncargadosTBL.getModel();
        int a =  modelo.getRowCount();
        
        for (int i = 0; i < a; i++) {
            modelo.removeRow(0);
        }
    }
