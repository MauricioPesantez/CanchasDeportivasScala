    private void modificarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarBtnActionPerformed
        // TODO add your handling code here:
        int a = tablaEncargadosTBL.getSelectedRow();
        String cedula = (String) modelo.getValueAt(a, 0);

        try {
            Encargado encargado ;
            encargado = EncargadoDao.getEncargado(cedula);

            AgregarEncargadoJPanel agregar = new AgregarEncargadoJPanel();
            agregar.ponerDatos(encargado);
            agregar.modificarBoton();

            GestionJFrame nueva = new GestionJFrame();
            nueva.agregarVentana(agregar);
            nueva.setVisible(true);

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }finally{
            this.hide();
        }

    }//GEN-LAST:event_modificarBtnActionPerformed

